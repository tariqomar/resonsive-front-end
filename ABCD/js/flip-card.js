var state = 1;
function flipCard() {
    if (state) {
        document.getElementById("front-friend-request").style.webkitTransform="rotateY(-180deg)";    
        document.getElementById("back-friend-request").style.webkitTransform="rotateY(0deg)";
        state = 0;
    } else {
        document.getElementById("front-friend-request").style.webkitTransform="rotateY(0deg)";    
        document.getElementById("back-friend-request").style.webkitTransform="rotateY(180deg)";
        state = 1;
    }        
}


